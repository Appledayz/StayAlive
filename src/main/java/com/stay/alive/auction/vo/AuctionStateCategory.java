package com.stay.alive.auction.vo;

public class AuctionStateCategory {
	private int auctionStateCategoryNo;
	private String auctionStateCategoryName;
	private String auctionStateCategoryDate;
	
	public int getAuctionStateCategoryNo() {
		return auctionStateCategoryNo;
	}
	public void setAuctionStateCategoryNo(int auctionStateCategoryNo) {
		this.auctionStateCategoryNo = auctionStateCategoryNo;
	}
	public String getAuctionStateCategoryName() {
		return auctionStateCategoryName;
	}
	public void setAuctionStateCategoryName(String auctionStateCategoryName) {
		this.auctionStateCategoryName = auctionStateCategoryName;
	}
	public String getAuctionStateCategoryDate() {
		return auctionStateCategoryDate;
	}
	public void setAuctionStateCategoryDate(String auctionStateCategoryDate) {
		this.auctionStateCategoryDate = auctionStateCategoryDate;
	}
}
